package com.test.admin;

public class RegImageDTO {
	
	private String hpseq;		//게시글 seq
	private String piseq;		//이미지 seq
	private String itseq;		//이미지 종류seq
	private String filename;	//파일명 
	
	
	public String getHpseq() {
		return hpseq;
	}
	public void setHpseq(String hpseq) {
		this.hpseq = hpseq;
	}
	public String getPiseq() {
		return piseq;
	}
	public void setPiseq(String piseq) {
		this.piseq = piseq;
	}
	public String getItseq() {
		return itseq;
	}
	public void setItseq(String itseq) {
		this.itseq = itseq;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	
	
	

}
